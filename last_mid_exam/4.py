class MyComplex:
    def __init__(self, a, b):
        if a[0] == '-':
            real_1, imaginary_1 = int(a[:2]), int(a[2:-1])
        else:
            real_1, imaginary_1 = int(a[0]), int(a[1:-1])
        
        if b[0] == '-':
            real_2, imaginary_2 = int(b[:2]), int(b[2:-1])
        else:
            real_2, imaginary_2 = int(b[0]), int(b[1:-1])
        print(real_1, real_2, imaginary_1, imaginary_2)
        calc_real = real_1*real_2 - imaginary_1*imaginary_2
        calc_imaginary = real_1*imaginary_2 + real_2*imaginary_1
        if calc_imaginary != '-':
            print(str(calc_real) + '+' + str(calc_imaginary) + 'i')
        else:
            print(str(calc_real) + str(calc_imaginary) + 'i')
        


